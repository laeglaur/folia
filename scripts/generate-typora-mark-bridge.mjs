import { readFile, writeFile } from 'node:fs/promises';

const manifest = JSON.parse(await readFile('src/styles/typora/manifest.json', 'utf8'));
const outputPath = 'src/styles/typora/generated/typora-mark-bridge.css';

const stripComments = (css) => css.replace(/\/\*[\s\S]*?\*\//g, '');

const readRules = (css) => {
  const rules = [];
  let selectorStart = 0;
  let depth = 0;
  let bodyStart = -1;

  for (let index = 0; index < css.length; index += 1) {
    const char = css[index];

    if (char === '{') {
      if (depth === 0) {
        bodyStart = index + 1;
      }
      depth += 1;
      continue;
    }

    if (char === '}') {
      depth -= 1;
      if (depth === 0 && bodyStart >= 0) {
        const selector = css.slice(selectorStart, bodyStart - 1).trim();
        const body = css.slice(bodyStart, index).trim();
        if (selector && body && !selector.startsWith('@')) {
          rules.push({ selector, body });
        }
        selectorStart = index + 1;
        bodyStart = -1;
      }
    }
  }

  return rules;
};

const splitSelectors = (selectorText) => selectorText
  .split(',')
  .map((selector) => selector.trim())
  .filter(Boolean);

const isDirectMarkSelector = (selector, themeId) => {
  const prefix = `.typora-theme[data-content-theme="${themeId}"] .typora-write`;
  if (!selector.startsWith(prefix)) return false;
  const tail = selector.slice(prefix.length).trim();
  return tail === 'mark' || tail === ':where(mark)';
};

const bridgeSelectorFor = (themeId) =>
  `.typora-theme[data-content-theme="${themeId}"] .typora-write .typora-block-doc li[data-type="taskItem"][data-todo-style="bracket"]`;

const blocks = [
  '/* Generated by scripts/generate-typora-mark-bridge.mjs. */',
  '/* Bridges Typora mark/highlight declarations onto notebook bracket todos. */'
];

for (const theme of manifest) {
  if (!theme.id?.startsWith('typora-') || !theme.scopedCss) continue;

  const css = stripComments(await readFile(theme.scopedCss, 'utf8'));
  const matchingBodies = readRules(css)
    .filter((rule) => splitSelectors(rule.selector).some((selector) => isDirectMarkSelector(selector, theme.id)))
    .map((rule) => rule.body);

  if (matchingBodies.length === 0) continue;

  blocks.push('');
  blocks.push(`${bridgeSelectorFor(theme.id)} {`);
  for (const body of matchingBodies) {
    for (const line of body.split('\n')) {
      const trimmed = line.trim();
      if (trimmed) blocks.push(`  ${trimmed}`);
    }
  }
  blocks.push('}');
}

blocks.push('');

await writeFile(outputPath, blocks.join('\n'));
console.log(`Wrote ${outputPath}`);
